package artoria.logging;

/**
 * Logger level.
 * @author Kahle
 */
public enum Level {

    /**
     * Level trace
     */
    TRACE,

    /**
     * Level debug
     */
    DEBUG,

    /**
     * Level info
     */
    INFO,

    /**
     * Level warn
     */
    WARN,

    /**
     * Level error
     */
    ERROR

}
